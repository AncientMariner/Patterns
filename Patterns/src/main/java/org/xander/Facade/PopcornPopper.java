package org.xander.Facade;

public class PopcornPopper {
    public void on() {
        System.out.println("Turning on the popcorn popper");
    }

    public void off() {
        System.out.println("Turning off the popcorn popper");
    }

    public void pop() {
        System.out.println("Making a popcorn");
    }
}